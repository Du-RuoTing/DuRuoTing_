abbr_cmd = on_regex(r"^abbr\s+([A-Za-z][A-Za-z0-9_-]*)$", priority=8, block=True)


@abbr_cmd.handle()
async def handle_abbr(event: GroupMessageEvent) -> None:
    raw = event.get_plaintext().strip()
    query = raw.split(maxsplit=1)[1].lower().replace("-", "").replace("_", "")
    schools = ABBR_MAP.get(query)
    if not schools:
        await abbr_cmd.finish(f"没有找到 {query.upper()} 对应的高校。")
    if len(schools) == 1:
        await abbr_cmd.finish(f"{query.upper()}：{schools[0]}")
    await abbr_cmd.finish(f"{query.upper()} 可能对应：\n" + "\n".join(f"- {school}" for school in schools))
